import Foundation

/// A set of predefined margin values using the 8-point grid system.
enum Margin {
    /// Extra small margin of 4 points.
    static let extraSmall: CGFloat = 4
    /// Small margin of 8 points.
    static let small: CGFloat = 8
    /// Medium margin of 16 points.
    static let medium: CGFloat = 16
    /// Large margin of 24 points.
    static let large: CGFloat = 24
}
